#!/usr/bin/env bash
set -euo pipefail

ENV_FILE="${PROVEO_ENV_FILE:-.env}"

PROVIDER_KEYS=(
  ANTHROPIC_API_KEY
  OPENAI_API_KEY
  OPENROUTER_API_KEY
  XAI_API_KEY
  GEMINI_API_KEY
  GOOGLE_API_KEY
  DEEPSEEK_API_KEY
  GROQ_API_KEY
  MISTRAL_API_KEY
  TOGETHER_API_KEY
  COHERE_API_KEY
  PERPLEXITY_API_KEY
  HUGGINGFACE_API_KEY
  HF_TOKEN
)

print_error() {
  echo "❌ $*" >&2
}

quote_env_value() {
  printf '%q' "$1"
}

if [[ -e "$ENV_FILE" ]]; then
  echo "✅ Found existing $ENV_FILE; leaving it unchanged."
  exit 0
fi

found_keys=()
for key in "${PROVIDER_KEYS[@]}"; do
  if [[ -n "${!key:-}" ]]; then
    found_keys+=("$key")
  fi
done

if (( ${#found_keys[@]} == 0 )); then
  print_error "No LLM API provider keys found in the current environment."
  echo "Set one of these before running 'proveo init':" >&2
  printf '  %s\n' "${PROVIDER_KEYS[@]}" >&2
  exit 1
fi

umask 077
{
  echo "# Generated by proveo init from API keys in the host environment."
  for key in "${found_keys[@]}"; do
    printf '%s=' "$key"
    quote_env_value "${!key}"
    printf '\n'
  done
} >"$ENV_FILE"

echo "✅ Created $ENV_FILE with ${#found_keys[@]} provider key(s): ${found_keys[*]}"
